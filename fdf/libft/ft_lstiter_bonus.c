#include "libft.h"

void ft_lstiter(t_list1 *lst, void (*f)(void *))
{
	if (!lst || !f)
		return;
	if (lst)
	{
		while (lst)
		{
			(*f)(lst->content);
			lst = lst->next;
		}
	}
}
